package uk.ac.ed.epcc.webapp.content;

/**
 * @author Stephen Booth
 *
 */
public class ScrollText implements UIGenerator {

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((text == null) ? 0 : text.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ScrollText other = (ScrollText) obj;
		if (text == null) {
			if (other.text != null)
				return false;
		} else if (!text.equals(other.text))
			return false;
		return true;
	}

	private final String text;
	/**
	 * 
	 */
 	public ScrollText(String text) {
		this.text=text;
	}

	/* (non-Javadoc)
	 * @see uk.ac.ed.epcc.webapp.content.UIGenerator#addContent(uk.ac.ed.epcc.webapp.content.ContentBuilder)
	 */
	@Override
	public ContentBuilder addContent(ContentBuilder builder) {
		ContentBuilder panel = builder.getPanel("scrollwrapper", "scrolltext");
		panel.addText(text);
		panel.addParent();
		return builder;
	}
	
	public String toString() {
		return text;
	}

}
