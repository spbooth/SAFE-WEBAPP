package uk.ac.ed.epcc.webapp.jdbc.config;

public class ConfigError extends Error {

	public ConfigError() {
	}

	public ConfigError(String arg0) {
		super(arg0);
	}

	public ConfigError(Throwable arg0) {
		super(arg0);
	}

	public ConfigError(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
