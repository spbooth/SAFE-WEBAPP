package uk.ac.ed.epcc.webapp.forms.html;

public class UnsupportedResultException extends Exception {

	public UnsupportedResultException() {
	}

	public UnsupportedResultException(String arg0) {
		super(arg0);
	}

	public UnsupportedResultException(Throwable arg0) {
		super(arg0);
	}

	public UnsupportedResultException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
